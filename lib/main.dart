void main() async {
  // Memastikan WidgetsBinding diinisialisasi sebelum mengakses database
  WidgetsFlutterBinding.ensureInitialized();
  
  // Set preferred orientations
  await SystemChrome.setPreferredOrientations([
    DeviceOrientation.portraitUp,
    DeviceOrientation.portraitDown,
  ]);
  
  // Inisialisasi printing dengan cara yang lebih aman
  try {
    await Printing.info();
  } catch (e) {
    print('Printing initialization error: $e');
    // Lanjutkan meskipun ada error, karena ini hanya untuk inisialisasi
  }
  
  // Inisialisasi database dengan penanganan error
  try {
    await DatabaseHelper.instance.database;
  } catch (e) {
    print('Database initialization error: $e');
    // Lanjutkan meskipun ada error, kita akan menangani error di screen
  }
  
  runApp(const MyApp());
}

